package thinkingJava.chapter7;

/**
 * 飞船控制类
 */
public class SpaceShipControl {
    void up(int step) {
        System.out.println("move up" + step);
    }

    void down(int step) {
        System.out.println("move down" + step);
    }

    void left(int step) {
        System.out.println("move left" + step);
    }

    void right(int step) {
        System.out.println("move right" + step);
    }

}
